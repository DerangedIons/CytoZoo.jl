@testset "ToRORd — numerical correctness vs ArmyHeart" begin
    model = ToRORd(; celltype = 0)

    u = copy(ARMYHEART_REF_U0)
    du = similar(u)

    model(du, u, nothing, 0.0)

    # Compare each derivative against ArmyHeart reference
    mismatches = Int[]
    for i in 1:length(du)
        ref = ARMYHEART_REF_DU[i]
        if ref == 0.0
            if du[i] != 0.0
                push!(mismatches, i)
            end
        else
            relerr = abs(du[i] - ref) / max(abs(ref), 1e-30)
            if relerr > 1e-10
                push!(mismatches, i)
            end
        end
    end

    if !isempty(mismatches)
        @warn "Mismatched derivatives at indices: $mismatches"
        for i in mismatches
            @warn "  du[$i]: CytoZoo=$(du[i]), ArmyHeart=$(ARMYHEART_REF_DU[i]), " *
                  "relerr=$(abs(du[i] - ARMYHEART_REF_DU[i]) / max(abs(ARMYHEART_REF_DU[i]), 1e-30))"
        end
    end

    @test isempty(mismatches)

    # Also test element-wise with tolerance
    for i in 1:length(du)
        @test du[i] ≈ ARMYHEART_REF_DU[i] atol = 1e-20 rtol = 1e-10
    end
end
